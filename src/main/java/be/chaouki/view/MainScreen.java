    private void addButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addButtonActionPerformed
        tableModel.addEmptyLine();
        jTable.setRowSelectionInterval(tableModel.getRowCount()-1, tableModel.getRowCount()-1); // auto-select the newly created line
        
        tableModel.fireTableRowsInserted(tableModel.getRowCount()-1, tableModel.getRowCount()-1);
        updateRowHeights(jTable);
        //updateTotalPrice();
    }//GEN-LAST:event_addButtonActionPerformed

    private void delButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_delButtonActionPerformed
        int rowInd=jTable.getSelectedRow();
        if(rowInd==-1){
            JOptionPane.showMessageDialog(null, "No line selected");
            return;
        }
        tableModel.removeLine(rowInd);
        
        tableModel.fireTableDataChanged();
        updateRowHeights(jTable);
        updateTotalPrice();
    }//GEN-LAST:event_delButtonActionPerformed

    private void resetButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_resetButtonActionPerformed
        tableModel.reset();
        tableModel.fireTableDataChanged();
        updateRowHeights(jTable);
        updateTotalPrice();
        
        // also revet to normal position the pizza size buttons
        normalPizzaButton.setSelected(true);
        normalPizzaButton1.setSelected(true);
        
        // and revet the discountCheckBoxes
        discount1CheckBox.setSelected(false);
        tableModel.setIsDiscount1Applic(false);
        discount2CheckBox.setSelected(false);
        tableModel.setIsDiscount2Applic(false);
        discount3CheckBox.setSelected(false);
        tableModel.setIsDiscount3Applic(false);
    }//GEN-LAST:event_resetButtonActionPerformed

